const int N = 1e6 + 100;

int k, l, r;

vector<int> prime;

void simpleSieve() {
    bool mark[N] = {};
 	
    for (int i = 2; i * i < N; ++i) {
        if (mark[i] == false)  {
            prime.push_back(i);
            for (int j = i; j < N; j += i)
                mark[j] = true;
        }
    }
}

vector<int> primesInRange(long long L, long long R) {
    // generate all primes up to sqrt(R)
    long long lim = sqrt(R);
    vector<bool> mark(lim + 1, false);
    vector<long long> primes;
    for (long long i = 2; i <= lim; ++i) {
        if (!mark[i]) {
            primes.emplace_back(i);
            for (long long j = i * i; j <= lim; j += i)
                mark[j] = true;
        }
    }

    vector<bool> isPrime(R - L + 1, true);
    for (long long i : primes)
        for (long long j = max(i * i, (L + i - 1) / i * i); j <= R; j += i)
            isPrime[j - L] = false;
    if (L == 1)
        isPrime[0] = false;
  

  	vector<int> ans;

  	for(int i = L; i <= R; i++) {
  		if(isPrime[i - L]) {
  			ans.push_back(i);
  		}
  	}
    return ans;
}


void solve() {
	cin >> k >> l >> r;

	vector<int> primes = primesInRange(l, r);

	assert(primes.size() >= k);

	int kthPrime = primes[k - 1];

	// debug(primes);

	for(int i : primes) {
		if(i > kthPrime) {
			break;
		}
		if(kthPrime % 10 == i % 10) {
			cout << i << " ";
		}
	}

	cout << "\n";
}

