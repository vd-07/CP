void solve() {
	string s;
	cin >> s;

	int total_mask = 0;

	for(char c : s) {
		if(c == '?') {
			continue;
		}

		int pos = c - 'a';

		total_mask ^= (1 << pos);
		// total mask = pa pb pc pd pe.....pz
	}

	int curr_mask = 0;
	map<int, int> countSubStrings;

	countSubStrings[0] = 1;

	long long ans = 0;

	for(char c : s) {
		int pos = (c == '?' ? 26 : c - 'a');

		curr_mask ^= (1 << pos);

		// now from i to l where l < i
		// need to have same parity as total_mask
		// total_mask = curr_mask ^ prev_mask_at_i
		// total_mask ^ curr_mask count

		for(int j = 0; j < 26; j++) {
			// i need odd no of ? (as even would do nothing, odd increases one tolerance level)
			// i am tolerating the current alphabet, so lets just reverse its parity (introduce one)
			ans += countSubStrings[curr_mask ^ total_mask ^ (1 << j) ^ (1 << 26)];
		}

		ans += countSubStrings[curr_mask ^ total_mask];

		countSubStrings[curr_mask]++;
	}

	cout << ans << "\n";
}

