const int mxN = 3e5 + 1;

int P, Q, S, N;
vector<vector<vector<int>>> dp;

int recur(int a, int b, int moves) {
	if(moves == 0) {
		return N;
	}

	debug(a, b, moves);

	if(dp[a][b][moves] != -1) {
		return dp[a][b][moves];
	}

	int v1 = abs(N - a - b);

	// fill

	int v2 = recur(P, b, moves - 1);

	int v3 = recur(a, Q, moves - 1);

	// empty

	int v4 = recur(0, b, moves - 1);

	int v5 = recur(a, 0, moves - 1);

	// transfer

	int v6 = recur(min(P, a + b), min(Q, max(P - a - b, 0)), moves - 1);

	int v7 = recur(min(P, max(Q - a - b, 0)), min(Q, a + b), moves - 1);

	return dp[a][b][moves] = min({v1, v2, v3, v4, v5, v6, v7});
}

void solve() {
	cin >> P >> Q >> S >> N;

	dp = vector<vector<vector<int>>> (P + 5, vector<vector<int>> (Q + 5, vector<int> (S + 5, -1)));

	cout << recur(0, 0, S);
}

