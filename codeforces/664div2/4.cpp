void solve() {
	int n, d, m;

	cin >> n >> d >> m;

	vector<long long> small, big;
	int ex;

	for(int i = 0; i < n; i++) {
		cin >> ex;
		if(ex > m) {
			big.push_back(ex);
		}
		else {
			small.push_back(ex);
		}
	}

	sort(small.rbegin(), small.rend());
	sort(big.rbegin(), big.rend());

	for(int i = 1; i < small.size(); i++) {
		small[i] += small[i - 1];
	}

	if(big.empty()) {
		cout << small.back();
		return;
	}

	for(int i = 1; i < big.size(); i++) {
		big[i] += big[i - 1];
	}


	long long ans = 0;
	for(int i = 0; i < big.size(); i++) {
		
		long long x = i + 1;
		long long noOfDays = 1LL * (x - 1) * (d + 1) + 1;
		noOfDays = n - noOfDays;

		if(noOfDays < 0) {
			break;
		}

		if(noOfDays > (long long)small.size()) {
			noOfDays = small.size();
		}

		noOfDays--;

		if(noOfDays > -1) {
			x = small[noOfDays];
		}
		else {
			x = 0;
		}

		ans = max(ans, big[i] + x);
		debug(noOfDays, x, ans, big[i]);
	}
	cout << ans;
}

