string reachTheEnd(vector<string> grid, int maxTime) {
	int n = grid.size();
	int m = grid[0].size();

	queue<array<int, 3>> currElement;
	currElement.push({0, 0, 1});

	vector<int> dx = {1, -1, 0, 0}, dy = {0, 0, -1, 1};

	int minTime = maxTime + 1;

	// debug(minTime);

	while(!currElement.empty()) {
		array<int, 3> curr = currElement.front();

		currElement.pop();

		int x = curr[0], y = curr[1];
		int timeTravelled = curr[2] + 1;

		if(x == n - 1 && y == m - 1) {
			minTime = curr[2];
			break;
		}

		for(int i = 0; i < 4; i++) {
			int nx = x + dx[i];
			int ny = y + dy[i];

			if(nx < n && nx >= 0 && ny < m && ny >= 0) {
				if(grid[nx][ny] == '.') {
					currElement.push({nx, ny, timeTravelled});
				}
			}
		}

	}

	// debug(minTime);

	return minTime <= maxTime ? "Yes" : "No";
}

