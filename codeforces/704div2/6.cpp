const int mxN = 1e3 + 1;

int n;
int a[mxN], b[mxN];

// + 5 min


void solve() {
	cin >> n;

	int B = 0;

	int maxE = 0;

	for(int i = 1; i <= n; i++) {
		cin >> a[i] >> b[i];

		B += b[i];
		maxE = max(maxE, a[i]);
	}

	vector<vector<int>> dp(n + 1, vector<int> (n * maxE + 1, -1));

	dp[0][0] = 0;

	for(int i = 1; i <= n; i++) {

		// vector<vector<int>> next_dp(dp.begin(), dp.end());

 		for(int k = i; k >= 1; k--) {

			for(int A = a[i]; A <= n * maxE; A++) {
				// dp[k][i][A] = dp[k][i - 1][A];
				if(dp[k - 1][A - a[i]] == -1) {
					continue;
				} 

				dp[k][A] = max(dp[k][A], b[i] + dp[k - 1][A - a[i]]);

			}

		}
	}

	for(int k = 1; k <= n; k++) {
		double ans = 0;
		for(int A = 1; A <= n * maxE; A++) {

			if(dp[k][A] == -1) continue;

			double curr = dp[k][A] + B;
			curr /= 2;

			double As = A;

			ans = max(ans, min(As, curr));

			debug(k, ans, A, curr);
		}
		cout << ans << " ";
	}

}

