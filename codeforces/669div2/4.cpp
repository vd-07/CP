int a[mxN];

int recur(int id, int minVal = a[1], int maxVal = a[1], int till = 1) {

	if(id >= n + 1) {
		if(till != n) {
			return INT_MAX;
		}
		return 0;
	}
	
	int v1 = INT_MAX, v2 = INT_MAX, v3 = INT_MAX;

	// for max
	if(a[id] >= maxVal) {
		v1 = 1 + recur(id + 1, a[id], a[id], id);
	}

	// for min
	if(a[id] <= minVal) {
		v2 = 1 + recur(id + 1, a[id], a[id], id);
	}

	minVal = min(a[id], minVal);
	maxVal = max(a[id], maxVal);

	// debug(id, v1, v2, v3);
	if(a[id] != a[till])
	v3 = recur(id + 1, minVal, maxVal, till);

	return min({v3, v2, v1});
	
}

