int countOfQM() {
	int count = 0;
	int ans = 0;

	for(char i : s) {
		if(i == '?') {
			++ count;
		} else {
			ans += (count * (count + 1)) / 2;
			count = 0;
		}
	}

	ans += (count * (count + 1)) / 2;

	return ans;
}

void solve() {

	cin >> s;

	n = s.size();

	int ans = 0;

	vector<vector<int>> dp(n, vector<int> (2));

	for(int i = 0; i < n; ++ i) {
		if(s[i] == '?' || s[i] == '1') {
			dp[i][1] = 1;

			if(i > 0) {
				dp[i][1] += dp[i - 1][0];
			}
		}

		if(s[i] == '?' || s[i] == '0') {
			dp[i][0] = 1;

			if(i > 0) {
				dp[i][0] += dp[i - 1][1];
			}
		}		

		ans += dp[i][0] + dp[i][1];

		debug(ans);
	}

	ans -= countOfQM();

	cout << ans << "\n";
}

