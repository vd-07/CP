void find(int n, int count = 0) {
	if(n == 0) {
		ans = min(count, ans);
		return;
	}

	if(n < 0) {
		return;
	}

	int idx = upper_bound(v.begin(), v.end(), n) - v.begin();

	debug(idx);

	int i1 = idx - 1;
	int i2 = idx - 2;

	if(i1 >= 0) {
		find(n - v[i1], count + 1);
	}

	if(i2 >= 0) {
		find(n - v[i2], count + 1);
	}

}

