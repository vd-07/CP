const int mxN = 3e5 + 1;

int n;
int a[mxN];

void solve() {
	cin >> n;

	int maxpref[n] = {};
	int sum = 0;

	for(int i = 0; i < n; i++) {
		cin >> a[i];

		maxpref[i] = a[i];

		sum += a[i];

		if(i != 0) {
			maxpref[i] = max(maxpref[i], maxpref[i - 1]);
		}
	}

	int maxsuff[n];

	maxsuff[n - 1] = a[n - 1];

	for(int i = n - 2; i >= 0; i--) {
		maxsuff[i] = max(a[i], maxsuff[i + 1]);
	}

	int ans = 0;

	for(int i = 0; i < n; i++) { 
		int currMax = 0;
		if(i != 0) {
			currMax = maxpref[i - 1];
		}
		if(i != n - 1) {
			currMax = max(maxsuff[i + 1], currMax);
		}

		int extra = (n - 1) * currMax - sum + a[i];

		// need to suffice extra using a[i]
		if(a[i] < extra) {
			ans = max(ans, extra - a[i]);
		} else {
			int left = a[i] - extra;

			int currAns = ((n - 1) - (left % (n - 1))) % (n - 1);

			ans = max(ans, currAns);
		}
		debug(ans, currMax, extra);
	}
	cout << ans << "\n";
}

