const int mxN = 3e5 + 1;

int n, m;
int a[mxN];

void solve() {
	cin >> n >> m;

	int matrix[n + 1][m + 1];

	for(int i = 1; i <= n; i++) {
		for(int j = 1; j <= m; j++) {
			cin >> matrix[i][j];
		}
	}


	int ans = 0;

	for(int i = 1; 2 * i <= n + 1; i++) {
		for(int j = 1; 2 * j <= m + 1; j++) {

			vector<int> v;

			v.push_back(matrix[i][j]);
			v.push_back(matrix[n - i + 1][j]);
			v.push_back(matrix[n - i + 1][m - j + 1]);
			v.push_back(matrix[i][m - j + 1]);

			int curr = INT_MAX;
			for(int value : v) {
				int temp = 0;

				temp += abs(matrix[i][j] - value);
				if(n - i + 1 != i)
					temp += abs(matrix[n - i + 1][j] - value);
				if(m - j + 1 != j)
					temp += abs(matrix[i][m - j + 1] - value);
				if(n - i + 1 != i && m - j + 1 != j)
					temp += abs(matrix[n - i + 1][m - j + 1] - value);	

				curr = min(curr, temp);
			}

			ans += curr;
			
		}
	}
	cout << ans << "\n";
}

