const int mxN = 3e5 + 1;

const int mod = 998244353;

int n, k;
int a[mxN], b[mxN];

void solve() {
	cin >> n >> k;

	set<int> s, t;

	int idx[n + 1] = {};

	// s : for seeing if neighbours are actual neighbours, some may have been removed earlier
	// t : for seeing if any neighbour is in b and not yet removed

	for(int i = 0; i < n; i++) {
		cin >> a[i];
		s.insert(a[i]);
		idx[a[i]] = i;
	}

	for(int i = 0; i < k; i++) {
		cin >> b[i];
		t.insert(b[i]);
	}

	int ans = 1;

	for(int i = 0; i < k; i++) {
		// find the location of b[i] in a[i]
		int j = idx[b[i]];

		// find the neighbours
		int j1 = j - 1;
		int j2 = j + 1;

		int mul = 2;

		if(j1 == -1 || j2 == n) {
			mul--;
		}

		if(j1 != -1 && t.find(a[j1]) != t.end()) {
			mul--;
		}

		if(j2 != n && t.find(a[j2]) != t.end()) {
			mul--;
		}

		ans *= mul;

		t.erase(b[i]);

		ans %= mod;
	}

	cout << ans << "\n";
}

