const int mxN = 3e5 + 1;

int n, k;
int a[mxN];

int process(vector<int> v1, vector<int> v2) {
	int sum = LLONG_MIN;

	int s1 = 1;

	debug(v1, v2);

	for(int i = -1; i < 5 && i < (int)v1.size(); i++) {
		if(i != -1) {
			s1 *= v1[i];
		}

		int s2 = 1;

		int ct = 4 - i;
		
		while(ct--) {
			if(ct >= v2.size()) {
				break;
			}
			s2 *= v2[ct];
		}

		debug(ct, s1, s2);

		if(ct == -1) {
			sum = max(s1 * s2, sum);
		}
	}
	debug(sum);
	return sum;
}

void solve() {
	cin >> n;

	vector<int> neg, pos;

	for(int i = 0; i < n; i++) {
		cin >> a[i];
		if(a[i] < 0) {
			neg.push_back(a[i]);
		} else {
			pos.push_back(a[i]);
		}
	}

	sort(neg.begin(), neg.end());
	sort(pos.begin(), pos.end());

	int ans = LLONG_MIN;

	ans = max(ans, process(neg, pos));

	reverse(neg.begin(), neg.end());

	ans = max(ans, process(neg, pos));

	reverse(pos.begin(), pos.end());

	ans = max(ans, process(neg, pos));

	reverse(neg.begin(), neg.end());

	ans = max(ans, process(neg, pos));

	cout << ans << "\n";

}

