const int mxN = 3e5 + 1;

int m;

void solve() {
	cin >> m;
	vector<vector<int>> a(2, vector<int> (m));
	vector<vector<int>> suffix(2, vector<int> (m)), prefix(2, vector<int> (m));

	for(int i = 0; i < 2; ++ i) {
		for(int j = 0; j < m; ++ j) {
			cin >> a[i][j];
		}
	}

	int ans = 1e10;
	suffix = a, prefix = a;

	for(int i = 1; i < m; ++ i) {
		prefix[0][i] += prefix[0][i - 1];
		prefix[1][i] += prefix[1][i - 1];
	}

	for(int i = m - 2; i >= 0; -- i) {
		suffix[0][i] += suffix[0][i + 1];
		suffix[1][i] += suffix[1][i + 1];
	}

	for(int j = 0; j < m; ++ j) {
		int curr = 0;
		if(j > 0) {
			curr = prefix[1][j - 1];
		}

		if(j < m - 1) {
			curr = max(curr, suffix[0][j + 1]);
		}

		ans = min(curr, ans);
	}

	// if(ans == 1e10) {
	// 	ans = 0;
	// }

	cout << ans << "\n";

}

