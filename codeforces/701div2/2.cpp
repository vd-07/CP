const int mxN = 3e5 + 1;

int n, q, k;
int a[mxN];

void solve() {
	cin >> n >> q >> k;

	for(int i = 1; i <= n; i++) {
		cin >> a[i];
	}

	// n > 1
	int prefix[n + 2] = {};
	a[n + 1] = k + 1;
	// strictly increasing

	prefix[1] = a[1] - 1 + (a[2] - a[1] - 1);

	for(int i = 2; i <= n; i++) {
		// go down
		prefix[i] = a[i] - a[i - 1] - 1;

		// go up
		prefix[i] += a[i + 1] - a[i] - 1;

		debug(prefix[i]);

		prefix[i] += prefix[i - 1]; 
	}

	int l, r;
	while(q--) {
		cin >> l >> r;

		// prefix[i] is for a[i - 1]
		int ans = (a[l] - 1) + (k - a[r]);

		debug(ans);

		if(l < r) {
			ans += a[l + 1] - a[l] - 1;
			ans += a[r] - a[r - 1] - 1;
		}

		debug(ans);

		if(r >= l + 2) {
			ans += prefix[r - 1] - prefix[l];	
		}
		

		cout << ans << "\n";
	}
}

