int a, b;

int cut(int a, int b) {
	int moves = 0;
	while(a) {
		a /= b;
		moves++;
	}
	return moves;
}

void solve() {
	cin >> a >> b;
	int moves = 0;
	while(a) {
		if(a / b == 0) {
			moves++;
			break;
		}


		if(b == 1) {
			b++;
			moves ++;
		}

		int op1 = cut(a, b);

		int op2 = cut(a, b + 1) + 1;

		if(op1 < op2) {
			a /= b;
		} else {
			b++;
		}
		
		moves++;
	}
	cout << moves << "\n";
}

